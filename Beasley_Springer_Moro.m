function z = Beasley_Springer_Moro()

    a0 = 2.50662823884;
    a1 = -18.61500062529;
    a2 = 41.39119773534;
    a3 = -25.44106049637;

    b0 = -8.47351093090;
    b1 = 23.08336743743;
    b2 = -21.06224101826;
    b3 = 3.13082909833;

    c0 = 0.3374754822726147;
    c1 = 0.9761690190917186;
    c2 = 0.1607979714918209;
    c3 = 0.0276438810333863;
    c4 = 0.0038405729373609;
    c5 = 0.0003951896511919;
    c6 = 0.0000321767881768;
    c7 = 0.0000002888167364;
    c8 = 0.0000003960315187;

    u = rand;
    y = u - 0.5;

    if abs(y) < 0.42  
        r = y .^ 2;
        z = y.*polyval([a3,a2,a1,a0],r)./polyval([b3,b2,b1,b0,1],r);
    else
        r = u;
        if y > 0
            r = 1 - u;
        end
        r = log(-log(r));
        z = polyval([c8,c7,c6,c5,c4,c3,c2,c1,c0],r);
        z = z .* sign(y);        
    end

end