
function y = cdf_hasting(x)
    b1 = 0.319381530;
    b2 = -0.356563782;
    b3 = 1.781477937;
    b4 = -1.821255978;
    b5 = 1.330274429;

    p = 0.2316419;
    c = 0.918938533204672;

    a = abs(x);
    t = 1 / (1 + a * p);
    s = polyval([b5, b4, b3, b2, b1, 0], t);
    y = s * exp(-0.5 * x * x - c);
    if x > 0
    	y = 1 - y;
    end