function y = cdf_marsaglia(x)

    v(1) = 1.253314137315500;
    v(2) = 0.6556795424187985; 
    v(3)= 0.4213692292880545;
    v(4) = 0.3045902987101033; 
    v(5) = 0.2366523829135607; 
    v(6) = 0.1928081047153158; 
    v(7) = 0.1623776608968675; 
    v(8) = 0.1401041834530502;
    v(9) = 0.1231319632579329;
    v(10) = 0.1097872825783083;
    v(11) = 0.09902859647173193;
    v(12) = 0.09017567550106468;
    v(13) = 0.08276628650136917;
    v(14) = 0.0764757610162485;
    v(15) = 0.07106958053885211;

    c = log(sqrt(2*pi));

    j = floor(min(abs(x) + 0.5, 14));
    z = j;
    h = abs(x) - z;
    a = v(j + 1);
    b = z * a - 1;
    q = 1;
    s = a + h * b;

    for i = 2:2:24-j
        a = (a + z * b) / i;
        b = (b + z * a) / (i + 1);
        q = q * h * h;
        s = s + q * (a + h * b);
    end

    y= s * exp(-0.5 * x * x - c);

    if x > 0
        y = 1 - y;
    end
end